# Output functions
print_task() { echo -e "    ${BLUE}▶${NC} $1..."; }
print_success() { echo -e "    ${GREEN}✓${NC} $1"; }
print_warning() { echo -e "    ${YELLOW}⚠${NC} $1"; }
print_error() { echo -e "    ${RED}✗${NC} $1"; }
print_critical() { echo -e "    ${RED}${BOLD}🔴 CRITICAL:${NC} $1"; }
print_info() { echo -e "    ${CYAN}ℹ${NC} $1"; }

print_vuln() {
    case "$1" in
        CRITICAL) echo -e "    ${RED}${BOLD}[CRITICAL]${NC} $2" ;;
        HIGH) echo -e "    ${RED}[HIGH]${NC} $2" ;;
        MEDIUM) echo -e "    ${YELLOW}[MEDIUM]${NC} $2" ;;
        LOW) echo -e "    ${BLUE}[LOW]${NC} $2" ;;
        INFO) echo -e "    ${CYAN}[INFO]${NC} $2" ;;
    esac
}

